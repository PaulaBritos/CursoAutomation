package ClaseTres;

public class EstructurasCondicionales {
    public static void main(String[] args) {

        //IF y ELSE-
        int valor1 = 200; //aca declaramos el tipo de dato y el dato
        int valor2 = 150;

        if (valor1 < valor2) { //acá decimos que si tal cosa se cumple, tiene que suceder tal cosa
            System.out.println("valor1 es menor que valor2");
        } else { //y acá decimos que si no se cumple, tiene que pasar esto otro
            System.out.println("valor1 es mayor que valor2");
        }
    }

        }



